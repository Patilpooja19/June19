package com.xworkz.Abstract.app;

public abstract class Specs {
	
	public abstract void see();
	public abstract void breakable();
	public abstract void foldable();
	public abstract void lens();
	public abstract void type();
	
	public void design() {
		System.out.println("design of specs");
	
	}
	
	public void frameless() {
		System.out.println("frameless of specs");
	
	}
	
	public void thickness() {
		System.out.println("thickness of specs");
	
	}
	
	public void brand() {
		System.out.println("brand of specs");
	
	}
	
	public void price() {
		System.out.println("price of specs");
	
	}
		
	

}
