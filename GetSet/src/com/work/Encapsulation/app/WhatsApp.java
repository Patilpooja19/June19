package com.work.Encapsulation.app;

public class WhatsApp {
	
	
		private String messageType;
		private long number;
		private boolean fingerPrint;
		private String theme;
		private int noOfGroup;
		private String call;
		private int noOfContacts;
		private int blockContacts;
		private String wallpaper;
		private int storage;
		private int fontSize;
		private String fontStyle;
		private String language;
		private int linkedDevice;

		public String getMessageType() {
			return this.messageType;
		}

		public void setMessageType(String messageType) {
			this.messageType = messageType;
		}

		public long getNumber() {
			return this.number;
		}

		public void setNumber(long number) {
			this.number = number;
		}

		public boolean isFingerPrint() {
			return this.fingerPrint;
		}

		public void setFingerPrint(boolean fingerPrint) {
			this.fingerPrint = fingerPrint;
		}

		public String getTheme() {
			return this.theme;
		}

		public void setTheme(String theme) {
			this.theme = theme;
		}

		public int getNoOfGroup() {
			return this.noOfGroup;
		}

		public void setNoOfGroup(int noOfGroup) {
			this.noOfGroup = noOfGroup;
		}

		public String getCall() {
			return this.call;
		}

		public void setCall(String call) {
			this.call = call;
		}

		public int getNoOfContacts() {
			return this.noOfContacts;
		}

		public void setNoOfContacts(int noOfContacts) {
			this.noOfContacts = noOfContacts;
		}

		public int getBlockContacts() {
			return this.blockContacts;
		}

		public void setBlockContacts(int blockContacts) {
			this.blockContacts = blockContacts;
		}

		public String getWallpaper() {
			return this.wallpaper;
		}

		public void setWallpaper(String wallpaper) {
			this.wallpaper = wallpaper;
		}

		public int getStorage() {
			return this.storage;
		}

		public void setStorage(int storage) {
			this.storage = storage;
		}

		public int getFontSize() {
			return this.fontSize;
		}

		public void setFontSize(int fontSize) {
			this.fontSize = fontSize;
		}

		public String getFontStyle() {
			return this.fontStyle;
		}

		public void setFontStyle(String fontStyle) {
			this.fontStyle = fontStyle;
		}

		public String getLanguage() {
			return this.language;
		}

		public void setLanguage(String language) {
			this.language = language;
		}

		public int getLinkedDevice() {
			return this.linkedDevice;
		}

		public void setLinkedDevice(int linkedDevice) {
			this.linkedDevice = linkedDevice;
		}

}
